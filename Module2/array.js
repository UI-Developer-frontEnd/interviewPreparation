// Arrays in javascript are ordered collection of data.

var arr =['Hemanth',26,true,69.38];
console.log(arr);

// access array elements using index numbers starting from "0"

console.log(arr[2]); // output: true
console.log(arr[0]); // output: Hemanth

// Replacing elements of array using indes values

arr[1] = "Hemanth Kumar";
console.log(arr); // output : ['Hemanth Kumar',26,true,69.38]